export function pickUnnamedChip(namedHexes: string[]): Chip {
  const remaining = CHIPS.filter((c) => !namedHexes.includes(c.hex))
  const pool = remaining.length > 0 ? remaining : CHIPS
  return pool[(Math.random() * pool.length) | 0]
}

export function luminance(rgb: [number, number, number]): number {
  return (0.299 * rgb[0] + 0.587 * rgb[1] + 0.114 * rgb[2]) / 255
}

export function uiColor(rgb: [number, number, number]): string {
  return luminance(rgb) > 0.55 ? 'rgba(30,30,30,0.9)' : 'rgba(255,255,255,0.9)'
}
